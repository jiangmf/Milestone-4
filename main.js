function convertDuration(duration) {
  var minutes = parseInt(duration / 60);
  var seconds = duration % 60;
  return [minutes, seconds]
}

function newPlaylist() {
  $("#playlistList").html("");
  var name = prompt("Enter Playlist name:", "Playlist");
  var value = {
    name: name,
    songs: []
  }
  playlists[name] = value;
  playlistSidebar();
}
